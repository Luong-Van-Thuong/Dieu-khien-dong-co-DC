package com.example.da5_ht;

public class Ob_TTNV {
    private int id;
    private String name;

    public Ob_TTNV() {
    }

    public Ob_TTNV(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
